package com.yu.yuplus.sound.config;

public class Area {
    String name;
    int topX;
    int topZ;
    int bottomX;
    int bottomZ;
    int topY;
    int bottomY;
    String world;


    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public int getTopX() {
        return topX;
    }

    public void setTopX(int topX) {
        this.topX = topX;
    }

    public int getTopZ() {
        return topZ;
    }

    public void setTopZ(int topZ) {
        this.topZ = topZ;
    }

    public int getBottomX() {
        return bottomX;
    }

    public void setBottomX(int bottomX) {
        this.bottomX = bottomX;
    }

    public int getBottomZ() {
        return bottomZ;
    }

    public void setBottomZ(int bottomZ) {
        this.bottomZ = bottomZ;
    }

    public int getTopY() {
        return topY;
    }

    public void setTopY(int topY) {
        this.topY = topY;
    }

    public int getBottomY() {
        return bottomY;
    }

    public void setBottomY(int bottomY) {
        this.bottomY = bottomY;
    }

    public String getWorld() {
        return world;
    }

    public void setWorld(String world) {
        this.world = world;
    }
}
